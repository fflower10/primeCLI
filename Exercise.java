package com.francesco.bean;

public class Exercise {
	
	private String code;
	private String description;
	private MuscleBand muscleBand;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public MuscleBand getMuscleBand() {
		return muscleBand;
	}
	public void setMuscleBand(MuscleBand muscleBand) {
		this.muscleBand = muscleBand;
	}
	
	

}
